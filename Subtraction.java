public class Subtraction {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;
        int result;

        result = a - b;

        System.out.println("First number = " + a);
        System.out.println("Second number = " + b);
        System.out.println("Subtraction = " + result);
    }
}

